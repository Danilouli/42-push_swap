#include "libft.h"

void	ft_intarr_rev(int **arr, int len)
{
	int i;

	i = 0;
	while (i < len / 2)
	{
		ft_swap_ints(&((*arr)[i]), &((*arr)[len - 1 - i]));
		i++;
	}
}
